/*
注释内容可以被JDK提供的工具javadoc所解析,
生成一套以网页文件形式体现的该程序的说明文档
*/

public class HelloJava {
  /*
  多行注释:
  如下的main方法是程序的入口！
  main的格式是固定的！
  */
  public static void main(String[] args) {
    // 单行注释: 如下语句表示输出到控制台
    System.out.println("Hello Java!");
  }
}
